    public void load(String maSv){
        try {
            StudentManager sm = new StudentManager();
            this.maSv = maSv;

            String name = sm.getDetail(maSv).getName();
            this.lbName.setText(name);
            this.lbMaSv.setText(maSv);
            this.list = sm.getListDiem(maSv);

            if(this.list.size() < 1){
                lbMessage.setText("Sinh Viên Không Có Điểm");
            } else {
                XemDiemThiSv.Model m = new XemDiemThiSv.Model();
                this.tbListDiem.setModel(m);
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(XemDiemThiSv.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(XemDiemThiSv.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

