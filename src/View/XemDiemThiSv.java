        @Override
        public Object getValueAt(int row, int column) {
            switch(column){
                case 0:
                    return list.get(row).getMaMonHoc();

                case 1:
                    return list.get(row).getTenMonHoc();

                case 2:
                    return list.get(row).getDiemThi();
                default:
                    return "";
            }
        }

